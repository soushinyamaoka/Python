    @classmethod
    def _get_aliases(cls):
        aliases = dict(cls.ALIASES)
        for k, v in EXTRA_ALIASES.items():
            if k in aliases:
                aliases[k] = list(set(aliases[k] + v))
            else:
                aliases[k] = v
        return aliases

    @classmethod
    def find_protein(cls, text):
        """テキストからタンパク質食材を検索"""
        text = text.strip()
        # 完全一致
        if text in PROTEINS:
            return text
        # エイリアス検索
        for alias, candidates in cls._get_aliases().items():
            if alias in text or text in alias:
                protein_candidates = [c for c in candidates if c in PROTEINS]
                if protein_candidates:
                    return random.choice(protein_candidates)
        # 部分一致
        for name in PROTEINS:
            if text in name or name in text:
                return name
        # カテゴリのタグ検索
        for name, data in PROTEINS.items():
            if text in data.get("tags", []) or text in data.get("category", ""):
                return name
        return None

    @classmethod
    def find_vegetable(cls, text):
        """テキストから野菜を検索"""
        text = text.strip()
        # 完全一致
        if text in VEGETABLES:
            return text
        # エイリアス検索
        for alias, candidates in cls._get_aliases().items():
            if alias in text or text in alias:
                veg_candidates = [c for c in candidates if c in VEGETABLES]
                if veg_candidates:
                    return random.choice(veg_candidates)
        # 部分一致
        for name in VEGETABLES:
            if text in name or name in text:
                return name
        # カテゴリのタグ検索
        for name, data in VEGETABLES.items():
            if text in data.get("tags", []) or text in data.get("category", ""):
                return name
        return None

    @classmethod
    def find_ingredient(cls, text):
        """タンパク質か野菜かを自動判別して検索"""
        p = cls.find_protein(text)
        if p:
            return ("protein", p)
        v = cls.find_vegetable(text)
        if v:
            return ("vegetable", v)
        return (None, None)


